public class Besatzung {
    private String tag;
    private String dienstbeginn;
    private String dienstdauer;
    private String name;
    private String funktion;
    private String telefon;
    private String id;

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getDienstbeginn() {
        return dienstbeginn;
    }

    public void setDienstbeginn(String dienstbeginn) {
        this.dienstbeginn = dienstbeginn;
    }

    public String getDienstdauer() {
        return dienstdauer;
    }

    public void setDienstdauer(String dienstdauer) {
        this.dienstdauer = dienstdauer;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFunktion() {
        return funktion;
    }

    public void setFunktion(String funktion) {
        this.funktion = funktion;
    }

    public String getTelefon() {
        return telefon;
    }

    public void setTelefon(String telefon) {
        this.telefon = telefon;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
